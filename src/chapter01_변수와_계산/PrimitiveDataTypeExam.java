package chapter01_변수와_계산;

public class PrimitiveDataTypeExam {

	public static void main(String[] args) {
		boolean isTrue = true;
		
		System.out.println(isTrue);
		
		boolean isFalse = false;
		
		System.out.println(isFalse);
		
		char c = 'f';
		
		int x = 59;
		
		long bing1 = 345655433L;
		
		long bing2 = 345655433l;
		
		float f1 = 32.4F;
		
		float f2 = 32.4f;
		
		double d = 3453567.5;
	}

}
